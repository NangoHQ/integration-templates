import { z } from 'zod';
import { createAction } from 'nango';

const GenericCrmIntegrationSchema = z.object({
    integrationId: z.number().describe('Integration ID generated by Gong'),
    name: z.string().describe('Integration name'),
    ownerEmail: z.string().describe('Email of the integration owner')
});

const OutputSchema = z.object({
    integrations: z.array(GenericCrmIntegrationSchema).optional(),
    requestId: z.string().optional().describe('A Gong request reference ID')
});

export default createAction({
    description: 'Retrieve details of the registered generic CRM integration',
    endpoint: {
        method: 'GET',
        path: '/actions/get-crm-integration'
    },
    input: z.object({}),
    output: OutputSchema,
    scopes: ['api:crm:integrations:read'],
    exec: async (nango, _input) => {
        // Docs: CRM -> Get Generic CRM integration details
        // https://help.gong.io/docs/what-the-gong-api-provides
        const config = {
            endpoint: '/v2/crm/integrations',
            retries: 3
        };

        // @allowTryCatch
        // The GET /v2/crm/integrations endpoint is plan-gated and may return 404
        // on standard accounts or when no generic CRM integrations exist.
        // It may also return 401 if the OAuth connection lacks the required scope.
        // We treat both as a valid empty result rather than a hard failure.
        try {
            const response = await nango.get(config);
            if (response.status === 404 || response.status === 401) {
                return {
                    integrations: []
                };
            }
            if (response.status !== 200) {
                throw new nango.ActionError({
                    message: `Unexpected HTTP status ${response.status}`,
                    errors: []
                });
            }
            const parsed = OutputSchema.safeParse(response.data);
            if (!parsed.success) {
                throw new nango.ActionError({
                    message: 'Invalid response from Gong API',
                    errors: parsed.error.issues
                });
            }
            return parsed.data;
        } catch (error) {
            if (
                typeof error === 'object' &&
                error !== null &&
                'response' in error &&
                typeof error.response === 'object' &&
                error.response !== null &&
                'status' in error.response &&
                (error.response.status === 404 || error.response.status === 401)
            ) {
                return {
                    integrations: []
                };
            }
            throw error;
        }
    }
});
